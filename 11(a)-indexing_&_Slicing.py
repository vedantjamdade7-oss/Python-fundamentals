# Tuple slicing

tuple = (10,20,30,40,50,60)

print(tuple[1:4])
print(tuple[:4])
print(tuple[0::2])
print(tuple[::3])
print(tuple[::-1])
print(tuple[-5:-1])
print(tuple[-1:-5:-1])